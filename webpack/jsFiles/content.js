jQuery(document).ready(function() {
  // O meni
  $('.o-meni-naslov').html(data.oMeni.naslov);
  $('.o-meni-tekst').html(data.oMeni.tekst);

  // O nama
  $('.o-nama-naslov').html(data.oNama.naslov);
  $('.o-nama-tekst').html(data.oNama.tekst);

  //   Proizvodi
  var ispisProizvoda = '';

  $('.proizvodi-naslov').html(data.proizvodi.naslov);

  for (var i = 0; i < data.proizvodi.lista.length; i++) {
    ispisProizvoda += `<div class="row">
    <div class="col-md-8 product product-${i + 1}"></div>
    <div class="col-md-4 product-text product-text-${i + 1}">
      <h3 class="first">
        <span>${data.proizvodi.lista[i].naslov}</span>
      </h3>
      <p>
      ${data.proizvodi.lista[i].tekst}
      </p>
      <div class="text-center button mb-1">
       ${vrste(i)}
      </div>
      <div class="text-center button">
      ${atest(i)}
      </div>
      ${galerija(i)}
    </div>
  </div>`;
  }

  function atest(broj) {
    if (data.proizvodi.lista[broj].atest) {
      return `<a href="${data.proizvodi.lista[broj].atest}" target="_blank">
        ATEST
      </a>`;
    }

    return '';
  }

  function vrste(broj) {
    var ispisVrsta = '';

    for (var i = 0; i < data.proizvodi.lista[broj].vrste.length; i++) {
      ispisVrsta += `<a
        href="${data.proizvodi.lista[broj].vrste[i].href}"
        data-fancybox="${data.proizvodi.lista[broj].vrste[i].dataFancybox}"
        >${data.proizvodi.lista[broj].vrste[i].ime}</a
      >`;
    }

    return ispisVrsta;
  }

  function galerija(broj) {
    var ispisGalerije = '';

    for (var i = 0; i < data.proizvodi.lista[broj].galerija.length; i++) {
      ispisGalerije += `<div class="d-none">
        ${ispisSlika(data.proizvodi.lista[broj].galerija[i])}
      </div>
    `;
    }

    return ispisGalerije;
  }

  function ispisSlika(galerija) {
    var ispisSlika = '';

    for (var i = 0; i < galerija.length; i++) {
      ispisSlika += `<a
      href="${galerija[i].slika}"
      data-fancybox="${galerija[i].ime}"
    >
    </a>
    `;
    }

    return ispisSlika;
  }

  $('#products__block').append(ispisProizvoda);

  //   Usluge
  var ispisUsluga = '';

  $('.usluge-naslov').html(data.usluge.naslov);

  for (var i = 0; i < data.usluge.vrste.length; i++) {
    ispisUsluga += `<div class="col-md-6 mb-3">
    <h3>
      <span>${data.usluge.vrste[i].naslov}</span>
      <div class="red-square"></div>
    </h3>
    <p>
    ${data.usluge.vrste[i].tekst}
    </p>
    <div class="button">
      <a href="${data.usluge.vrste[i].galerija.dugme.slika}" data-fancybox="${
      data.usluge.vrste[i].galerija.dugme.ime
    }"
        >VIDI VIŠE</a
      >
    </div>
    <div class="d-none">
        ${ispisSlikaUsluge(data.usluge.vrste[i].galerija.slike)}
    </div>
  </div>`;
  }

  function ispisSlikaUsluge(slike) {
    ispisSlika = '';

    for (var i = 0; i < slike.length; i++) {
      ispisSlika += `<a href="${slike[i].slika}" data-fancybox="${slike[i].ime}"> </a>`;
    }

    return ispisSlika;
  }

  $('.usluge-row').append(ispisUsluga);

  //   Prijatelji
  var ispisPrijatelja = '';

  $('.prijatelji-naslov').html(data.prijatelji.naslov);

  for (var i = 0; i < data.prijatelji.vrste.length; i++) {
    ispisPrijatelja += `<div class="col-xs-6 col-sm-3 col-md-3">
    <a href="${data.prijatelji.vrste[i].link}" target="_blank">
      <img src="${data.prijatelji.vrste[i].slika}" alt="${data.prijatelji.vrste[i].slika}" />
    </a>
  </div>`;
  }

  $('.prijatelji-row').append(ispisPrijatelja);

  //   Ispis poruka

  var ispisPoruka = '';

  $('.prijatelji-naslov').html(data.prijatelji.naslov);

  for (var i = 0; i < data.poruke.length; i++) {
    ispisPoruka += `<div class="alert__block alert__block-${data.poruke[i].klasa}">
  <span>${data.poruke[i].poruka}</span>
</div>`;
  }

  $('.alerts').append(ispisPoruka);

  //   Ispis mape
  $('#map__block').append(`<iframe
    class="google_map"
    src="https://www.google.com/maps/embed?pb=!1m18!1m12!1m3!1d2814.5581610025747!2d20.05238771590571!3d45.135288579098436!2m3!1f0!2f0!3f0!3m2!1i1024!2i768!4f13.1!3m3!1m2!1s0x475afdb71d010999%3A0x67912d4720f7b1e3!2sLIMAKS!5e0!3m2!1ssr!2srs!4v1539622094135"
    height="450"
    scrolling="no"
    frameborder="0"
    style="border:0"
    allowfullscreen
    ></iframe>`);

  // Ispis navigacije
  var ispisNavigacije = '';
  var ispisMobilneNavigacije = '';

  for (var i = 0; i < data.navigacija.length; i++) {
    ispisNavigacije += `<a href="${kreirajLink(
      data.navigacija[i].href
    )}" class="scroll">${data.navigacija[i].ime}</a>`;
    ispisMobilneNavigacije += `<div class="menu__item">
    <a
      data-toggle="collapse"
      data-target="#demo"
      href="${data.navigacija[i].href}"
      class="scroll"
      >${data.navigacija[i].ime}</a
    >
  </div>`;
  }

  $('.navigation__block-inner').append(ispisNavigacije);
  $('.menu__items').append(ispisMobilneNavigacije);

  function kreirajLink(link) {
    if (window.location.pathname === '/about.html') {
      return `/index.html${link}`;
    }

    return link;
  }
});
